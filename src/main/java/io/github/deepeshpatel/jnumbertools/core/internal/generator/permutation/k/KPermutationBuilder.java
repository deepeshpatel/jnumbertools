package io.github.deepeshpatel.jnumbertools.core.internal.generator.permutation.k;

import io.github.deepeshpatel.jnumbertools.core.external.Calculator;
import io.github.deepeshpatel.jnumbertools.core.internal.generator.base.Builder;
import io.github.deepeshpatel.jnumbertools.core.internal.generator.base.EveryMthIterable;
import io.github.deepeshpatel.jnumbertools.core.internal.generator.numbers.BigIntegerChoice;
import io.github.deepeshpatel.jnumbertools.core.internal.generator.numbers.BigIntegerSample;

import java.math.BigInteger;
import java.util.List;
import java.util.Random;

/**
 * Builder for generating k-permutations of a list of elements.
 * <p>
 * A k-permutation (also called partial permutation) is an ordered arrangement of k elements
 * selected from a set of n distinct elements, where k ≤ n. The total number of k-permutations
 * is given by P(n,k) = n!/(n-k)!.
 * </p>
 *
 * <h2>Ordering Types</h2>
 * <p>
 * This builder supports two ordering strategies:
 * <ul>
 *   <li><b>Lexicographical Order</b> - Generated by {@link #lexOrder()}. Permutations are ordered
 *       based on the natural order of element indices.</li>
 *   <li><b>Combination Order</b> - Generated by {@link #combinationOrder()}. Permutations are first
 *       grouped by their underlying combination, then by permutations within each group.</li>
 * </ul>
 * </p>
 *
 * <h2>Usage Examples</h2>
 *
 * <h3>Basic Generation</h3>
 * <pre>
 * KPermutationBuilder&lt;String&gt; builder = new KPermutationBuilder&lt;&gt;(
 *     List.of("A", "B", "C", "D"), 2, calculator);
 *
 * // All 2-permutations in lexicographical order
 * builder.lexOrder()
 *        .forEach(System.out::println);
 * // Output: [A,B], [A,C], [A,D], [B,A], [B,C], [B,D], [C,A], [C,B], [C,D], [D,A], [D,B], [D,C]
 *
 * // Same permutations in combination order (grouped by combination)
 * builder.combinationOrder()
 *        .forEach(System.out::println);
 * // Output: [A,B], [B,A], [A,C], [C,A], [A,D], [D,A], [B,C], [C,B], [B,D], [D,B], [C,D], [D,C]
 * </pre>
 *
 * <h3>Every mᵗʰ Permutation</h3>
 * <pre>
 * // Every 3rd 2-permutation starting from rank 1
 * builder.lexOrderMth(3, 1)
 *        .forEach(System.out::println);
 * </pre>
 *
 * <h3>Random Sampling</h3>
 * <pre>
 * // Sample 4 unique permutations without replacement
 * builder.sample(4)
 *        .forEach(System.out::println);
 *
 * // Sample 5 permutations with replacement (duplicates allowed)
 * builder.choice(5)
 *        .forEach(System.out::println);
 * </pre>
 *
 * <h3>Permutations at Specific Ranks</h3>
 * <pre>
 * // Get permutations at ranks 0, 5, and 10
 * builder.byRanks(List.of(BigInteger.ZERO,
 *                         BigInteger.valueOf(5),
 *                         BigInteger.valueOf(10)))
 *        .forEach(System.out::println);
 * </pre>
 *
 * <h3>Integer Range Input</h3>
 * <pre>
 * // 2-permutations from set {0,1,2,3,4}
 * KPermutationBuilder&lt;Integer&gt; intBuilder = new KPermutationBuilder&lt;&gt;(5, 2, calculator);
 * intBuilder.lexOrder().forEach(System.out::println);
 * </pre>
 *
 * <p>
 * This builder is immutable and thread-safe. All configuration methods return new instances.
 * </p>
 *
 * @param <T> the type of elements in the permutations
 * @see KPermutationLexOrder
 * @see KPermutationCombinationOrder
 * @see KPermutationByRanks
 * @see KPermutationCombinationOrderMth
 * @author Deepesh Patel
 */
public final class KPermutationBuilder<T> implements Builder<T> {

    private final List<T> elements;
    private final int k;
    private final Calculator calculator;

    /**
     * Constructs a builder for k-permutations.
     *
     * @param elements the list of elements to permute (e.g., [A, B, C])
     * @param k the size of each permutation (kₖ); must be non-negative and ≤ nₙ
     * @param calculator utility for combinatorial calculations
     * @throws IllegalArgumentException if kₖ is negative or exceeds nₙ
     */
    public KPermutationBuilder(List<T> elements, int k, Calculator calculator) {
        this.elements = elements;
        this.k = k;
        this.calculator = calculator;
    }

    /**
     * Generates all k-permutations in lexicographical order.
     *
     * @return a {@link KPermutationLexOrder} instance for lexicographical iteration
     */
    public KPermutationLexOrder<T> lexOrder() {
        return new KPermutationLexOrder<>(elements, k);
    }

    /**
     * Returns the total number of k-permutations.
     *
     * @return the value of Pₙ,ₖ as a {@link BigInteger}
     */
    public BigInteger count() {
        return calculator.nPr(elements.size(), k);
    }

    @Override
    public boolean isEmpty() {
        return elements.isEmpty() || k == 0;
    }

    /**
     * Generates a random sample of k-permutations with replacement using custom random generator.
     * <p>
     * Each permutation is chosen independently and uniformly at random from all possible
     * ⁿPₖ permutations. The same permutation may appear multiple times in the result.
     * This is equivalent to generating sampleSize random ranks in [0, ⁿPₖ) with replacement.
     * </p>
     *
     * @param sampleSize the number of permutations to generate (must be > 0)
     * @param random the random generator to use
     * @return a generator producing random k-permutations (duplicates allowed)
     * @throws IllegalArgumentException if sampleSize ≤ 0 or random is null
     */
    @Override
    public KPermutationByRanks<T> choice(int sampleSize, Random random) {
        BigInteger max = calculator.nPr(elements.size(), k);
        var choice = new BigIntegerChoice(max, sampleSize, random);
        return new KPermutationByRanks<>(elements, k, choice, calculator);
    }

    /**
     * Generates k-permutations randomly without replacement using custom random generator.
     * <p>
     * The total number of k-permutations is Pₙ,ₖ = n!/(n−kₖ)!.
     * </p>
     *
     * @param sampleSize the number of permutations to generate; must be positive and ≤ Pₙ,ₖ
     * @param random the random generator to use
     * @return a {@link KPermutationByRanks} instance for random sampling without replacement
     * @throws IllegalArgumentException if sampleSize is not positive or exceeds Pₙ,ₖ or random is null
     */
    @Override
    public KPermutationByRanks<T> sample(int sampleSize, Random random) {
        BigInteger max = calculator.nPr(elements.size(), k);
        var sample = new BigIntegerSample(max, sampleSize, random);
        return new KPermutationByRanks<>(elements, k, sample, calculator);
    }

    /**
     * Generates k-permutations at specified lexicographical ranks.
     *
     * @param ranks an iterable of 0-based rank numbers (0 ≤ rank < Pₙ,ₖ)
     * @return a {@link KPermutationByRanks} instance for generating k-permutations at specified ranks
     * @throws IllegalArgumentException if any rank is negative or ≥ Pₙ,ₖ
     */
    public KPermutationByRanks<T> byRanks(Iterable<BigInteger> ranks) {
        validateByRanksParams(ranks);
        return new KPermutationByRanks<>(elements, k, ranks, calculator);
    }

    /**
     * Generates all k-permutations in combination order.
     *
     * @return a {@link KPermutationCombinationOrder} instance for combination-order iteration
     */
    public KPermutationCombinationOrder<T> combinationOrder() {
        return new KPermutationCombinationOrder<>(elements, k);
    }

    /**
     * Generates every mᵗʰ k-permutation in lexicographical order.
     *
     * @param m the step size for selecting every mᵗʰ permutation; must be positive
     * @param start the starting rank (0-based); must be non-negative
     * @return a {@link KPermutationByRanks} instance for mᵗʰ permutations in lex order
     * @throws IllegalArgumentException if m or start is invalid
     */
    public KPermutationByRanks<T> lexOrderMth(BigInteger m, BigInteger start) {
        validateLexOrderMthParams(m,start, count());
        EveryMthIterable iterator = new EveryMthIterable(start, m, calculator.nPr(elements.size(), k));
        return new KPermutationByRanks<>(elements, k, iterator, calculator);
    }

    /**
     * Generates every mᵗʰ k-permutation in combination order, using long values.
     *
     * @param m the step size for selecting every mᵗʰ permutation; must be positive
     * @param start the starting rank (0-based); must be non-negative
     * @return a {@link KPermutationCombinationOrderMth} instance for mᵗʰ permutations in combination order
     * @throws IllegalArgumentException if m or start is invalid
     */
    public KPermutationCombinationOrderMth<T> combinationOrderMth(long m, long start) {
        return combinationOrderMth(BigInteger.valueOf(m), BigInteger.valueOf(start));
    }

    /**
     * Generates every mᵗʰ k-permutation in combination order.
     *
     * @param m the step size for selecting every mᵗʰ permutation; must be positive
     * @param start the starting rank (0-based); must be non-negative
     * @return a {@link KPermutationCombinationOrderMth} instance for mᵗʰ permutations in combination order
     * @throws IllegalArgumentException if m or start is invalid
     */
    public KPermutationCombinationOrderMth<T> combinationOrderMth(BigInteger m, BigInteger start) {
        validateLexOrderMthParams(m,start, count());
        return new KPermutationCombinationOrderMth<>(elements, k, m, start, calculator);
    }

    @Override
    public String toString() {
        return "KPermutationBuilder{" +
                "elements=" + elements +
                ", k=" + k +
                ", count=" + count() +
                '}';
    }
}
